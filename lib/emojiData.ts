// Get random items from a specific tier
export function getRandomItemsFromTier(
  tier: 'easy' | 'medium' | 'hard',
  count: number
): EmojiItem[] {
  const tierItems = getItemsByDifficulty(tier);
  const shuffled = [...tierItems].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}
